def search_element():
    query = entry.get().strip()

    # Normalize input for flexible searching
    query_symbol = query.capitalize()
    query_name = query.title()
    query_number = query

    found = None

    for num, data in elements.items():
        # Check symbol
        if query_symbol == data["symbol"]:
            found = (num, data)
            break
        
        # Check name
        if query_name == data["name"]:
            found = (num, data)
            break
        
        # Check atomic number
        if query_number.isdigit() and int(query_number) == num:
            found = (num, data)
            break

    if not found:
        messagebox.showinfo("Not Found", f"No element found for: {query}")
        return

    num, data = found

    # Helper function for None -> "Unknown"
    def safe(value):
        return "Unknown" if value in (None, "", [], {}) else value

    # Convert lists to readable strings
    isotopes = ", ".join(data["isotopes"]) if data["isotopes"] else "None"
    uses = ", ".join(data["uses"]) if data["uses"] else "None"
    oxidation_states = ", ".join(map(str, data["oxidation_states"])) if data["oxidation_states"] else "None"

    info_text.set(f"""
Name: {safe(data['name'])}
Symbol: {safe(data['symbol'])}
Atomic Number: {num}
Atomic Mass: {safe(data['mass'])}

Group: {safe(data['group'])}
Period: {safe(data['period'])}
Category: {safe(data['category'])}

Electron Configuration:
{safe(data['config'])}

Melting Point (K): {safe(data['melting_point_K'])}
Boiling Point (K): {safe(data['boiling_point_K'])}
Density (g/cm³): {safe(data['density_g_per_cm3'])}

Electronegativity (Pauling): {safe(data['electronegativity_pauling'])}
Oxidation States: {oxidation_states}

Phase at STP: {safe(data['phase_at_STP'])}
Discovery: {safe(data['discovery'])}

Color: {safe(data['color'])}
Appearance: {safe(data['appearance'])}

Isotopes: {isotopes}
Uses: {uses}
""")


